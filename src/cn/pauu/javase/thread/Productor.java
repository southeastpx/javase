package cn.pauu.javase.thread;

/*
 * 生产者
 */
public class Productor implements Runnable {
	private Student s;
	private int x = 0;

	public Productor(Student s) {
		this.s = s;
	}

	@Override
	public void run() {
		while (true) {
			if (x % 2 == 0) {
				s.set("林青霞", 27);
			} else {
				s.set("刘意", 27);
			}
			x++;
		}
	}
}
